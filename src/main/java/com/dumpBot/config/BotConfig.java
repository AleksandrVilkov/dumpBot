package com.dumpBot.config;

public class BotConfig {
    private String token;
    private String name;

    public String getToken() {
        return token;
    }

    public void setToken(String value) {
        this.token = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String value) {
        this.name = value;
    }

    public BotConfig() {
    }
}
